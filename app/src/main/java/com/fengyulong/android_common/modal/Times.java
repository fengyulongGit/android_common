package com.fengyulong.android_common.modal;

/**
 * 时间对象
 *
 * @author ranfl
 */
public class Times {

    private long day;
    private long hours;
    private long minutes;
    private long second;

    public long getDay() {
        return day;
    }

    public void setDay(long day) {
        this.day = day;
    }

    public long getHours() {
        return hours;
    }

    public void setHours(long hours) {
        this.hours = hours;
    }

    public long getMinutes() {
        return minutes;
    }

    public void setMinutes(long minutes) {
        this.minutes = minutes;
    }

    public long getSecond() {
        return second;
    }

    public void setSecond(long second) {
        this.second = second;
    }

}
